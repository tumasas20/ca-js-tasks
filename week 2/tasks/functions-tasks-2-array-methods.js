console.log('---');
{
  function powerArrBy2(arr) {
    const square = [];
    let i = 0;
    while (i < numbers.length) {
      square.push(arr[i] ** 2);
      i++;
    }
    return square;
  }
  console.log({
    numbers,
    result: powerArrBy2(numbers)
  });
}
