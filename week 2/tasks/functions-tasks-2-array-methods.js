const numbers = [1, 2, -2, 6, -5, 9, 1.02, 45, -69, 77, -12, 2, 8, -2, -4, 59, 7, -3];

console.group('1. Padauginti masyvo narius iš 2 ir išsaugoti naujame masyve');
console.log('---');
{
  function mulArrBy2(arr) {
    const doubled = [];
    arr.forEach(nr => {
      doubled.push(nr * 2);
    });

    return doubled;
  }
  console.log({
    numbers,
    result: mulArrBy2(numbers)
  });
}
